            เพื่อบรรเทาอาการและป้องกันการเกิดภาวะแทรกซ้อนที่เป็นอันตรายต่อชีวิต ดังนั้น
            หากสังเกตพบอาการที่บ่งชี้ว่าไตมีปัญหา ควรรีบไปพบแพทย์เพื่อรับการตรวจรักษาให้ทันการณ์</p>
        <p>สาเหตุโรคไต</p>
        <p>1.กลั้นปัสสาวะบ่อยๆ สาเหตุโรคไตข้อนี้ทำให้เพิ่มความเสี่ยงต่อการติดเชื้อในทางเดินปัสสาวะ</p>
        <p>2.รับประทานยาหรือสมุนไพรอย่างไม่ระมัดระวัง เนื่องจากยาและสมุนไพรหลายชนิดส่งผลต่อการไหลเวียนเลือดมาเลี้ยงไต</p>
        <p>3.ดื่มน้ำน้อย สาเหตุโรคไตข้อนี้ส่งผลให้ปริมาณเลือดในร่างกายลดลง ปริมาณเลือดที่ไหลเวียนไปเลี้ยงอวัยวะสำคัญลดลง</p>
        <p>4.รับประทานอาหารไขมันสูง สาเหตุโรคไตข้อนี้ส่งผลให้เกิดการสะสมของไขมันที่ผนังหลอดเลือดทั่วร่างกาย
            สิ่งที่เป็นอันตราย คือ การมีไขมันสะสมที่ผนังหลอดเลือดหัวใจ สมอง และไต เพราะจะส่งผลให้เกิดการตีบแคบของหลอดเลือด
            มีปริมาณเลือดไหลเวียนมาที่อวัยวะนั้นลดลง</p>
        <p>5.รับประทานอาหารรสเค็มจัด เนื่องจากอาหารรสจัดมีการใช้เครื่องปรุงแต่ละชนิดในปริมาณมาก
            เพื่อทำให้เกิดรสชาติที่เข้มข้น และแน่นอน เครื่องปรุงเหล่านั้นจะขาดเกลือหรือเกลือโซเดียม ไม่ได้
            เกลือโซเดียมจะกระตุ้นให้ไตทำงานหนักขึ้น ส่งผลให้เพิ่มปริมาณเลือดไหลเวียนในร่างกาย ทำให้ไตทำงานลดลง</p>
        <p>6.สูบบุหรี่ สารในบุหรี่ส่งผลให้เกิดการเปลี่ยนแปลงของหลอดเลือด หากหลอดเลือดที่ไปเลี้ยงไตมีการเปลี่ยนแปลง</p>
        <p>7.ดื่มสุราหรือเครื่องดื่มที่มีแอลกอฮอล์เป็นประจำ สาเหตุโรคไตข้อนี้จะทำให้ร่างกายมีการสูญเสียน้ำมากขึ้น
            เนื่องจากแอลกอฮอล์เป็นตัวเร่งให้ร่างกายขับน้ำเพิ่มขึ้น จึงส่งผลให้ไตเสียหน้าที่จาก 2 สาเหตุ ได้แก่ เกิดการขาดน้ำ
            และไตทำหน้าที่มากกว่าปกติ</p>
