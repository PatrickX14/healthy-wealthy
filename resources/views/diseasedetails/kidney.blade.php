        <h1>โรคไต</h1>
        <p style="text-indent: 50px;">ภาวะที่ทำให้ไตเกิดความเสียหายหรือทำงานผิดปกติจนเกิดอาการเจ็บป่วยต่าง ๆ ตามมา
            เนื่องจากไตไม่สามารถฟอกเลือดหรือขับของเสียออกจากเลือดได้ตามปกติ หากป่วยเป็นโรคไต
