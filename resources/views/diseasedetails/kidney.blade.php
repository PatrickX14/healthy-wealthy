            <li>ควบคุมคาร์โบไฮเดรต หากผู้ป่วยต้องจำกัดโปรตีนต่ำมากๆ
                อาจต้องควบคุมคาร์โบไฮเดรตจากข้าวบางชนิดที่มีโปรตีนอยู่ด้วย</li>
